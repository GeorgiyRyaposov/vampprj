/// <summary>
/// Интерфейс состояния глобальной стейт-машины.
/// Чтобы добавить новое состояние: создайте класс в папке States, реализующий IState (Enter, Exit, Update),
/// и переключайтесь на него через GameStateMachine.EnterState(new MyState(...)) из другого состояния или из кода.
/// </summary>
public interface IState
{
    /// <summary>
    /// Вызывается при входе в состояние. Инициализация, подписки, загрузка сцены и т.д.
    /// </summary>
    void Enter();

    /// <summary>
    /// Вызывается при выходе из состояния. Отписки, выгрузка, сохранение.
    /// </summary>
    void Exit();

    /// <summary>
    /// Вызывается каждый кадр, пока состояние активно. Игровая логика, обновление UI.
    /// </summary>
    void Update();
}
